function importedProduct(
  code: number,
  name: string,
  category: Category,
  series: string,
  tags: string[],
  description: string,
): Product {
  return {
    id: `sp-${code}`,
    code: `${code}`.padStart(4, '0'),
    name,
    category,
    series,
    price: 30000,
    material: 'Resin 3D cao cấp',
    rating: 5,
    reviews: 20 + (code % 30),
    badge: code === 300 ? 'Mới' : undefined,
    inStock: true,
    accent: accentByCategory[category],
    image: `./products/sp-${code}.webp`,
    tags,
    description,
    highlights: [
      'Tạo hình nhân vật tự nhiên',
      'Chi tiết trang phục sắc nét',
      'Phù hợp trưng bày và dựng diorama',
    ],
  }
}

function visualProduct(
  code: number,
  name: string,
  category: Category,
  series: string,
  tags: string[],
): Product {
  const displayName = name.charAt(0).toLocaleLowerCase('vi') + name.slice(1)
  return importedProduct(
    code,
    name,
    category,
    series,
    tags,
    `Mô hình ${displayName} với tư thế tự nhiên và chi tiết trang phục rõ nét, phù hợp chủ đề ${series.toLocaleLowerCase('vi')}.`,
  )
}

