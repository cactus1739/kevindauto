const preOrderCodes = new Set([2211, 2224, 2228, 2229, 2230, 2233, 2234, 2235, 2236, 2237, 2238, 2292, 2293])

function p(code: number, name: string, category: Category, series = 'Đời thường'): Product {
  const displayName = name.charAt(0).toLocaleLowerCase('vi') + name.slice(1)
  const preOrder = preOrderCodes.has(code)
  return {
    id: `sp-${code}`,
    code: `${code}`,
    name,
    category,
    series,
    price: 30000,
    material: 'Resin 3D cao cấp',
    rating: 5,
    reviews: 20 + (code % 30),
    badge: preOrder ? 'Pre-order' : undefined,
    inStock: !preOrder,
    accent: accents[category],
    image: `./products/sp-${code}.webp`,
    tags: [...new Set([category, series.toLocaleLowerCase('vi'), ...displayName.toLocaleLowerCase('vi').split(/\s+/)])],
    description: `Mô hình ${displayName} với tạo hình rõ nét, phù hợp trưng bày và dựng diorama chủ đề ${series.toLocaleLowerCase('vi')}.`,
    highlights: ['Tạo hình rõ nét', 'Chi tiết bối cảnh sinh động', 'Phù hợp trưng bày và dựng diorama'],
  }
}

